package com.digitalinka.restpreventa.model;

public class AvancePolitica {
    private String codPolitica;
    private String descPolitica;
    private Double montoPolitica;


    public String getCodPolitica() {
        return codPolitica;
    }

    public void setCodPolitica(String codPolitica) {
        this.codPolitica = codPolitica;
    }

    public String getDescPolitica() {
        return descPolitica;
    }

    public void setDescPolitica(String descPolitica) {
        this.descPolitica = descPolitica;
    }

    public Double getMontoPolitica() {
        return montoPolitica;
    }

    public void setMontoPolitica(Double montoPolitica) {
        this.montoPolitica = montoPolitica;
    }
}
